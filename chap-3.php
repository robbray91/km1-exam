        <h1>Chapter 3: Analysing the Business Need</h1>

        <p>All organizations can improve. Change is needed to meet evolving needs. Sometimes projects replace IT systems without assessing real needs, leading to problems. Ensure solutions match real needs.</p>

        <hr>

        <h2>Business Analysis</h2>

        <p>Business analysis began in the early 1990s due to:</p>

        <ol>
            <li>Users unhappy with IT quality and communication.</li>
            <li>Realization of IT supporting business.</li>
            <li>Outsourcing growth, requiring better requirements.</li>
            <li>Adoption of systems thinking for successful IT-enabled changes.</li>
        </ol>

        <p>Business analysis has evolved from translation to proactive problem-solving. It emphasizes understanding root causes and holistic thinking. Senior analysts advise on business growth, considering non-IT solutions when necessary.</p>

        <hr>

        <h2>Definition of business analysis</h2>

        <p>Business analysis involves being an internal consultant to improve business systems by identifying options, defining requirements, and ensuring effective information system use to meet business needs. It enables change and value.</p>

        <hr>

        <h2>The Place of Business Analysis in the Development Lifecycle</h2>

        <p>Business analysis is essential across the Systems Development lifecycle, from investigating issues to defining requirements, testing, and realizing benefits. It's dynamic and adapts to both Waterfall and Agile methods.</p>

        <hr>

        <h2>Feasibility study as part of business analysis</h2>

        <p>During the feasibility study, business analysts play a crucial role in understanding the business situation, identifying issues, and evaluating options. They focus on aligning solutions with the organization's strategy and needs. To create a solid business case, financial, business, and technical aspects are assessed. This stage is essential for ensuring projects address the right issues and deliver value.</p>

        <hr>

        <h2>Define requirements for business analysis</h2>

        <p>During the "Define Requirements" stage, business analysts play a vital role in making sure the project understands and defines both business and technical requirements. These are split into functional (what the system should do) and non-functional (how well it should do it) requirements. Techniques like interviews and workshops are used to gather detailed information from business staff. The level of detail in requirements depends on the project's approach, with linear models needing more detailed requirements and evolutionary models allowing for less detailed ones. Business analysts follow a framework called Requirements Engineering for this work.</p>

        <hr>

        <h2>Delivering solution for business analysis</h2>

        <h3>Deliver Solution:</h3>
        <ul>
            <li>This part is about getting and setting up a solution to a problem, especially when it involves computer stuff.</li>
            <li>They use documents to decide which computer programs to use.</li>
        </ul>

        <h3>Design, Development, and Testing:</h3>
        <ul>
            <li>When we create or improve a computer system, we plan, create, and check it.</li>
            <li>Business analysts make sure changes won't cause problems and explain what the computer people need to do.</li>
            <li>They check things to make sure they fit and meet the needs.</li>
        </ul>

        <h3>User Acceptance Testing (UAT):</h3>
        <ul>
            <li>People who use the computer system every day check if it works like it should.</li>
            <li>Business analysts help them create tests and decide what to check.</li>
            <li>They use drawings and tables to figure out what to test.</li>
        </ul>

        <h3>Implementation:</h3>
        <ul>
            <li>When we start using the new system, we need a good plan and training.</li>
            <li>Business analysts help with this to make sure everyone understands the new system.</li>
        </ul>

        <h3>Post-implementation Review and Benefits Realization:</h3>
        <ul>
            <li>After we're done, we look back to see what went well, what we've learned, and if there are issues.</li>
            <li>We check if we're getting the benefits we wanted.</li>
            <li>Business analysts provide info and suggest what to do if we're not getting the benefits we wanted. They make sure computer systems work well for a business.</li>
        </ul>

        <hr>

        <h2>Outcomes from business analysis</h2>

        <ul>
            <li>Business analysis solves big problems in a holistic way.</li>
            <li>Changes involve IT/IS, process, people, and organization.</li>
            <li>IT/IS ensures tech aligns with business.</li>
            <li>Process changes streamline operations.</li>
            <li>People adapt to new tech and skills.</li>
            <li>Organization changes structure and communication for business efficiency.</li>
            <li>Business analysts ensure smooth transitions.</li>
        </ul>
