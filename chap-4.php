        <h1>Chapter 4: Making a Business Case</h1>

        <h2>The purpose of a business case</h2>
        <ol>
            <li>Address Business Needs: Identify project's role in meeting business objectives, from cost reduction to compliance.</li>
            <li>Justify: Prove the project's worth, comparing benefits to costs and risks.</li>
            <li>Governance: A guiding document throughout the project, ensuring alignment with initial goals and providing a reference point for decision-making.</li>
        </ol>

        <hr>

        <h2>The Business Case and the Development Lifecycle</h2>
        <ul>
            <li>Evaluate project's worth through costs and benefits.</li>
            <li>Define project requirements and reevaluate costs.</li>
            <li>Choose to build or buy, assess compatibility, and recheck costs.</li>
            <li>Develop, test, and ensure system fits the purpose.</li>
            <li>Continuously assess benefits, adapt to changes, and adjust plans as needed.</li>
        </ul>

        <hr>

        <h2>Elements of the Business Case</h2>
        <ul>
            <li>Introduction: Overview of the business case.</li>
            <li>Management Summary: Brief persuasive summary for top decision-makers.</li>
            <li>Background: Historical context for the issue or opportunity.</li>
            <li>Options: Listing choices and detailing the preferred one.</li>
            <li>Benefits: Expected positive outcomes.</li>
            <li>Costs: Short-term and long-term financial considerations.</li>
            <li>Impacts: Other vital information for decision-makers.</li>
            <li>Risks: Identifying potential problems and solutions.</li>
            <li>Investment Appraisal: Financial analysis and profitability timeline.</li>
            <li>Conclusions and Recommendations: Final suggestions or decision.</li>
            <li>Appendices: Additional detailed information, like technical data.</li>
        </ul>

        <hr>

        <h2>Identify, evaluate and selecting options for a business case</h2>
        <ol>
            <li>Identify Options: Explore all actions, assess their feasibility, and consider the "do nothing" choice.</li>
            <li>Shortlist Options: Narrow choices to 3-4 realistic ones, including "do nothing."</li>
            <li>Evaluate Shortlist: Assess options based on predefined criteria, emphasizing the preferred one's costs and benefits.</li>
            <li>Include in Business Case: Detailed info on shortlisted options goes into the business case, aiding decision-making and justification.</li>
        </ol>

        <hr>

        <h2>Cost benefit analysis</h2>
        <p>Cost-benefit analysis in the business case distinguishes between tangible (quantifiable) and intangible (hard-to-measure) costs and benefits, with consideration of timing. Common examples:</p>
        <h3>Costs:</h3>
        <ul>
            <li>Hardware/software expenses</li>
            <li>Labor costs</li>
            <li>Maintenance/support</li>
            <li>Training</li>
            <li>Opportunity costs</li>
            <li>Legal/regulatory compliance</li>
            <li>License fees</li>
        </ul>
        <h3>Benefits:</h3>
        <ul>
            <li>Efficiency/productivity</li>
            <li>Cost savings</li>
            <li>Revenue growth</li>
            <li>Customer satisfaction</li>
            <li>Competitive edge</li>
            <li>Staff morale</li>
            <li>Organizational image</li>
        </ul>
        <p>Balancing tangible and intangible elements is crucial, as intangible benefits can have long-term value.</p>

        <hr>

        <h2>Risk Analysis</h2>
        <ol>
            <li>Introduction to Risk Analysis:
                <ul>
                    <li>Every project, including computer and technology-related ones, involves risks.</li>
                    <li>Risks can vary in severity, like deciding whether to buy a new video game.</li>
                </ul>
            </li>
            <li>Types of Risks:
                <ul>
                    <li>There are two main types of risks:
                        <ul>
                            <li>Tangible Risks: Real and measurable problems related to a project.</li>
                            <li>Intangible Risks: Abstract problems that can affect your feelings, creativity, or connections with others.</li>
                        </ul>
                    </li>
                </ul>
            </li>
            <li>Examples of Tangible Costs:
                <ul>
                    <li>Money spent on the game, time to install and learn the game, buying new hardware, acquiring necessary software, and related expenses.</li>
                </ul>
            </li>
            <li>Examples of Tangible Benefits:
                <ul>
                    <li>Saving money and having less stuff to manage.</li>
                </ul>
            </li>
            <li>Examples of Intangible Costs:
                <ul>
                    <li>Feeling frustrated during setup, losing time for other activities, or needing to find new friends to play with.</li>
                </ul>
            </li>
            <li>Examples of Intangible Benefits:
                <ul>
                    <li>Feeling happier, making new friends, being more creative, and solving problems more easily.</li>
                </ul>
            </li>
            <li>Types of Risks (Project and Business Risks):
                <ul>
                    <li>Project Risks: Problems related to the game itself, like glitches or a confusing story.</li>
                    <li>Business Risks: How the game might affect your life or reputation, such as what your friends think of you.</li>
                </ul>
            </li>
            <li>Understanding Risks:
                <ul>
                    <li>To analyze risks effectively, consider:
                        <ul>
                            <li>Description: What could go wrong?</li>
                            <li>Impact: How bad it would be if it happens?</li>
                            <li>Scale of Impact: How much damage it could do (small, medium, big, or catastrophic).</li>
                            <li>Probability: How likely is it to happen?</li>
                            <li>Avoidance Actions: Actions to lower the chance of risk.</li>
                            <li>Mitigation Actions: Actions to lessen the damage if the risk occurs.</li>
                            <li>Owner: The person responsible for handling the risk.</li>
                        </ul>
                    </li>
                </ul>
            </li>
            <li>Importance of Risk Analysis:
                <ul>
                    <li>Understanding and managing risks helps senior managers (like parents deciding whether to buy the game) make better decisions.</li>
                </ul>
            </li>
        </ol>

        <hr>

        <h2>Impact Analysis</h2>
        <ol>
            <li>Introduction: Understanding Impacts
                <ul>
                    <li>Impacts are things senior managers consider when making important decisions, like managing a super cool game.</li>
                </ul>
            </li>
            <li>Different People:
                <ul>
                    <li>To improve the game, they might need to bring in new players with special skills.</li>
                    <li>This requires finding and inviting new teammates to join the game.</li>
                </ul>
            </li>
            <li>New Rules:
                <ul>
                    <li>Sometimes, the game may introduce new rules, changing how it's played.</li>
                    <li>Managers must teach players the new rules and offer rewards for playing well.</li>
                </ul>
            </li>
            <li>New Leadership:
                <ul>
                    <li>Big bosses might need to change their leadership style.</li>
                    <li>Instead of giving orders, they become mentors who support and help the players.</li>
                </ul>
            </li>
            <li>Importance of Knowing Impacts:
                <ul>
                    <li>Understanding these impacts is crucial for managers to handle them effectively and ensure the game or project's success.</li>
                    <li>It's similar to ensuring everyone knows the new game rules and plays well together.</li>
                </ul>
            </li>
        </ol>

        <hr>

        <h2>Investment Appraisal Techniques</h2>
        <ol>
            <li>Introduction: Investment Appraisal Techniques
                <ul>
                    <li>Investment appraisal techniques help people decide if it's smart to invest their money in projects, like starting a business.</li>
                    <li>Think of it like deciding if a lemonade stand project is worth the investment.</li>
                </ul>
            </li>
            <li>Payback or Break-even:
                <ul>
                    <li>Payback is about figuring out when you'll make back all the money you spent.</li>
                    <li>Example: If you spent $100 on your lemonade stand and made $20 every week, it would take 5 weeks to get your $100 back.</li>
                </ul>
            </li>
            <li>Time Value of Money:
                <ul>
                    <li>The Payback method doesn't consider that money you get in the future is worth less than money you have now.</li>
                    <li>For instance, $100 today is worth more than $100 a year from now.</li>
                </ul>
            </li>
            <li>Discounted Cash Flow (DCF) or Net Present Value (NPV):
                <ul>
                    <li>DCF adjusts future money to see how much it's worth today, considering the time difference.</li>
                    <li>If the final number is positive, it's a good investment; if it's negative, it's not a good idea.</li>
                </ul>
            </li>
            <li>Internal Rate of Return (IRR):
                <ul>
                    <li>IRR is a special number that indicates how good your investment is.</li>
                    <li>If IRR is higher than the interest you'd pay to borrow money, it's a good deal.</li>
                </ul>
            </li>
            <li>Applying the Techniques:
                <ul>
                    <li>You can use these techniques when deciding to start a lemonade stand to make wise financial choices.</li>
                    <li>Payback helps you know when you'll get your money back, DCF considers the time value of money, and IRR tells you how good the investment is.</li>
                </ul>
            </li>
        </ol>

