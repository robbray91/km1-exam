        <h2>Development Methodologies</h2>

        <h3>1. Starting a Project:</h3>
        <ul>
            <li>Creating a computer program or game is like baking cookies; you need a plan.</li>
            <li>"Development methodologies" are like recipes for success in the computer world.</li>
        </ul>

        <h3>2. Common Methodology Parts:</h3>
        <ul>
            <li><strong>Lifecycle</strong>: It's the big plan that breaks down all the steps from start to finish.</li>
            <li><strong>Process</strong>: Explains what to do at each step, like deciding on characters and levels in game design.</li>
            <li><strong>Roles</strong>: Like assigning jobs to people in a team, e.g., designing characters or writing code.</li>
            <li><strong>Deliverables</strong>: These are things you make at each step, such as character sketches or code pieces.</li>
            <li><strong>Techniques</strong>: These are the methods or tools used, similar to using a rolling pin to flatten cookie dough.</li>
        </ul>

        <h3>3. Two Types of Methodologies:</h3>
        <ul>
            <li><strong>Defined Processes</strong>: It's like a strict recipe with clear steps and predictability, but can be a bit too strict.</li>
            <li><strong>Empirical Processes</strong>: More like experimenting, allowing for changes as you go, and being flexible and creative.</li>
        </ul>

        <h3>4. Agile Methodology:</h3>
        <ul>
            <li>Agile is all about flexibility and working closely with users.</li>
            <li>It's like baking cookies with friends where you can adjust the recipe as you bake.</li>
            <li>Popular for making computer programs due to its adaptability and improvements as you go.</li>
        </ul>

        <h3>5. Choosing the Right Recipe:</h3>
        <ul>
            <li>When making your program, pick a methodology that fits your project.</li>
            <li>Some are strict, like following a set plan, while others are more like experimenting and being creative.</li>
        </ul>

