        <h1>Purpose of Systems Development:</h1>
        <ul>
            <li>Goal: Transform business needs into a functional computer system.</li>
            <li>Like turning a business dream into a reality using computers.</li>
        </ul>

        <h2>Development Process:</h2>
        <ul>
            <li>Steps:</li>
            <ul>
                <li>Check feasibility (Is it worth it?).</li>
                <li>Understand business requirements (What do they want?).</li>
                <li>Design the system (Plan it out).</li>
                <li>Develop or program the system.</li>
                <li>Test for bugs and ensure it works.</li>
                <li>Implement and release to the world.</li>
            </ul>
        </ul>

        <h3>Additional Steps:</h3>
        <ul>
            <li>Choose computer type (hardware procurement).</li>
            <li>Set up the system (installation).</li>
        </ul>

        <h2>Significance:</h2>
        <ul>
            <li>In the past, computer systems were made randomly.</li>
            <li>Now, organized steps ensure smoother development.</li>
            <li>Vital for companies; enables creation of larger and more advanced systems.</li>
        </ul>

        <hr>

        <h1>SYSTEMS DEVELOPMENT AND OTHER DISCIPLINES</h1>

        <h2>Project Management (The Planner):</h2>
        <ul>
            <li>Plan who does what.</li>
            <li>Gather tools.</li>
            <li>Ensure teamwork.</li>
        </ul>

        <h2>Business Analysis (The Investigator):</h2>
        <ul>
            <li>Solve problems or find opportunities.</li>
            <li>Investigate what makes the game special.</li>
        </ul>

        <h2>Systems Architecture (The Organizer):</h2>
        <ul>
            <li>Organize the game like a city.</li>
            <li>Create a blueprint for smooth organization.</li>
        </ul>

        <h2>Programming (The Coder):</h2>
        <ul>
            <li>Turn plans into a working game.</li>
            <li>Use codes for movement and actions.</li>
        </ul>

        <h2>Testing (The Game Checker):</h2>
        <ul>
            <li>Play the game early.</li>
            <li>Find and fix mistakes or bugs.</li>
        </ul>

        <h2>Configuration Management and Change Control (The Keepers of Order):</h2>
        <ul>
            <li>Organize game components.</li>
            <li>Control changes carefully.</li>
        </ul>

        <h2>Quality Control and Quality Assurance (The Quality Police):</h2>
        <ul>
            <li>Check the game meets standards.</li>
            <li>Ensure proper checking processes.</li>
        </ul>

        <h2>Service Management (The Game Caretaker):</h2>
        <ul>
            <li>Maintain servers.</li>
            <li>Fix issues and add features post-release.</li>
        </ul>

        <p>In short, making a game is a teamwork effort with different roles to ensure a smooth process and a successful game.</p>

        <hr>

        <h1>OFFSHORING AND OUTSOURCING OF SYSTEMS DEVELOPMENT</h1>

        <h2>Offshoring:</h2>
        <p>Imagine your club is like a party organizer. Offshoring is when you invite talented members from other neighborhoods to help with events. It's cost-effective, like bringing in people from India or Eastern Europe. But, it can be challenging because of language barriers and different traditions.</p>

        <h2>Outsourcing:</h2>
        <p>Think of hiring a party planning company for your club events. Instead of doing everything yourself, let experts handle it. They might take over the whole planning, easing your stress and potentially costing less. However, your club loses direct control, and knowledgeable members might work for the company instead.</p>

        <h2>Combined Approach:</h2>
        <p>Sometimes clubs do bothâ€”hire experts from far away. It's like a double benefit, but finding the right balance is crucial.</p>

        <h2>Challenges:</h2>
        <p>With offshoring, communication may take longer, and understanding each other can be tough. Outsourcing means less direct control over activities, and knowledgeable members might work for the planning company, not the club.</p>

        <h2>Balancing Act:</h2>
        <p>Finding the sweet spot between getting help from afar and keeping control is like maintaining the perfect balance for your club's fun activities.</p>

